# Gauss-Legendre quadrature rules on [-1, 1] and their tensor products.
# Ported from MATLAB: gauss_quadrature.m

"""
    gauss_rule(n) -> (pts::Vector{Float64}, wts::Vector{Float64})

Return the n-point Gauss-Legendre quadrature rule on [-1, 1].
Supported: n = 1, 2, …, 20, 30, 40, 50, 60.
"""
function gauss_rule(n::Int)::Tuple{Vector{Float64}, Vector{Float64}}
    return _GAUSS_RULES[n]
end

"""
    gauss_product(n, npd) -> Vector{Tuple{Vector{Float64}, Float64}}

Return the npd-dimensional tensor-product Gauss rule with n points per direction.
Each entry is (point_coords::Vector, weight::Float64).
"""
function gauss_product(n::Int, npd::Int)::Vector{Tuple{Vector{Float64}, Float64}}
    pts1d, wts1d = gauss_rule(n)
    ngp = n^npd
    result = Vector{Tuple{Vector{Float64}, Float64}}(undef, ngp)
    for igp in 1:ngp
        gp = zeros(npd)
        gw = 1.0
        denom = 1
        for i in 1:npd
            ig = mod((igp - 1) ÷ denom, n) + 1
            gp[i] = pts1d[ig]
            gw   *= wts1d[ig]
            denom *= n
        end
        result[igp] = (gp, gw)
    end
    return result
end

