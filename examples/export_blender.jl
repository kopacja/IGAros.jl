# export_blender.jl
#
# Export IGA geometry (NURBS patches, control points, control net) to OBJ/PLY
# files for Blender rendering.  Companion Blender Python script is generated
# by `write_blender_script()`.
#
# Usage (from pressurized_sphere_deltoidal.jl or any solver):
#
#   include("export_blender.jl")
#   export_blender("sphere", npc, nsd, npd, p_mat, n_mat, KV, P, B;
#                   U=U, ID=ID, E=E, nu=nu, n_vis=6,
#                   scalar_fn=my_stress_fn, scalar_name="von_mises")
#
# This creates:
#   sphere_patch_1.obj ... sphere_patch_N.obj   (tessellated NURBS surfaces)
#   sphere_cpnet_1.obj ... sphere_cpnet_N.obj   (control net as wireframe)
#   sphere_cpts.obj                              (control points as vertices)
#   sphere_import.py                             (Blender Python import script)

